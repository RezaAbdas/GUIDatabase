    private void tombolhitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tombolhitungActionPerformed
        //jika data filed kosong
        if (txtnama.getText().isEmpty() || txtjabatan.getText().isEmpty() || txtnip.getText().isEmpty() || gajipokok.getText().isEmpty()
                || tunjangan.getText().isEmpty() || lembur.getText().isEmpty() || bpjs.getText().isEmpty() || pajak.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Data tidak boleh kosong !!!!");
        }
        
        //deklarasi variabel
        double gaji_pokok = Double.parseDouble(gajipokok.getText()),
                tunjangan = Double.parseDouble(this.tunjangan.getText()),
                lembur =  Double.parseDouble(this.lembur.getText()),
                bpjs = Double.parseDouble(this.bpjs.getText()),
                pajak = Double.parseDouble(this.pajak.getText()),
                total_pemasukan,total_pengurangan,gaji_bersih;
        
        //hitung total pemasukan 
        total_pemasukan = gaji_pokok + tunjangan + lembur;
        totpemasukan.setText(""+ total_pemasukan);
        
        //hitung total pengeluaran
        total_pengurangan= bpjs + pajak;
        totpengurangan.setText("" + total_pengurangan);
        
        //hitung total gaji
        gaji_bersih = total_pemasukan - total_pengurangan;
        gajibersih.setText("" + gaji_bersih);
        
        
        //menampilkan data ke form
        output.setText("NIP\t : " + txtnip.getText() + "\nNama\t : " + txtnama.getText() + 
                "\nJabatan\t : " + txtjabatan.getText() + "\nPemasukan\t : Rp " + total_pemasukan +
                "\nPengurangan\t : Rp " + total_pengurangan + "\nGaji Bersih\t : Rp " + gaji_bersih);
        
       
        //memasukan data ke dalam database
           //menyimpan data yang telah diinput pada form
        try {

            /**
             * membuat variabel sql untuk memasukan data yang telah diinput ke
             * database perintah input data sama seperti di command mysql
             * menggunakan perintah INSERT INTO DATABASES VALUE
             */
            String sql = "INSERT INTO gaji_karyawan VALUES ('" + txtnip.getText() + "','" + txtnama.getText() + "','" + txtjabatan.getText() + "','" + total_pemasukan + "','" + total_pengurangan+ "','" + gaji_bersih +  "')";

          

            /**
             * *
             * membuat koneksi ke database ini harus dibuat dimasing-masing
             * event Konfig = class java yang berisi konfigurasi koneksi ke
             * database configDB() merupakan fungsi dari kelas Konfig
             */
            java.sql.Connection conn = (Connection) Koneksi.configDB();

            //membuat statement untuk merepresentasikan perintah sql setelah terjadi koneksi ke database
            java.sql.PreparedStatement pstm = conn.prepareStatement(sql);

            //untuk mengeksekusi statement
            pstm.execute();

            //pesan jika proses penyimpanan berhasil, this untuk memunculkan kotak dialog berada di atas form
            JOptionPane.showMessageDialog(this, "Data berhasi diunggah ke database");
            
        } catch (HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());

        }
    }//GEN-LAST:event_tombolhitungActionPerformed

