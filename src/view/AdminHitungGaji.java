    private void txtnipMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_txtnipMouseReleased
        ceknip();
         try {
            Connection con = Koneksi.configDB(); //buat koneksi
            Statement s = con.createStatement(); //buat statement untuk menampung query sql

            String sql = "SELECT * FROM karyawan where nip= '" + txtnip.getText() + "'"; //query sql untuk mendapatkan id dan password
            ResultSet r = s.executeQuery(sql); //hasil dari query swl diatas

            if (r.next()) {
                txtnama.setText(r.getString("nama"));
                txtjabatan.setText(r.getString("jabatan"));
            } else {
                JOptionPane.showMessageDialog(this, "NIP tidak terdaftar");
                
            }
        } catch (SQLException e) {
            System.out.println("Error" + e.getMessage());
        }
    }//GEN-LAST:event_txtnipMouseReleased

